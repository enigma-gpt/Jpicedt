package jpicedt.format.output.dxf;
import  jpicedt.format.output.dxf.DXFStringBuffer;

import java.util.ArrayList;

/**
 * Interface de tout formatteur de dessin donnée, permettant le formattage des
 * informations DXF (marque déposée) propre à ce dessin. Cette interface est appélée par les
 * objets de classe étendant  l'interface DXFInformation
 * @see {@link jpicedt.format.output.dxf.DXFInformation}
 * @since jPicEdt 1.6
 */
interface DXFInfoFormatter
{
	/**
	 * Récupère l'information EXTMIN formattable
	 * @since jPicEdt 1.6
	 */
	ArrayList<DXFInformation>  getExtMin();

	/**
	 * Récupère l'information EXTMAX formattable
	 * @since jPicEdt 1.6
	 */
	ArrayList<DXFInformation>  getExtMax();

	/**
	 * Récupère la partie ENTITIES formatté.
	 * @since jPicEdt 1.6
	 */
	DXFStringBuffer getEntities();
}

