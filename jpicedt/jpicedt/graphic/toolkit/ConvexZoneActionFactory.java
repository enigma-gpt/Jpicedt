package jpicedt.graphic.toolkit;

import javax.swing.Action;
import jpicedt.graphic.view.HitInfo;

/**
 * Permet à un <code>ConvexZoneGroup</code>, en interaction avec une <code>PopupMenuFactory</code>, de
 * produire des <code>Action</code>'s qui ont du sens avec ce <code>ConvexZoneGroup</code>.
 * @author <a href="mailto:vincentb1@users.sourceforge.net">Vincent Belaïche</a>
 * @since jPicEdt 1.6
 * @version $Id: ConvexZoneActionFactory.java,v 1.2 2013/03/27 06:59:41 vincentb1 Exp $
 */
public interface ConvexZoneActionFactory{
	/**
	 * Crée une liste de <code>PEAction</code>'s en relation à un <code>ConvexZoneGroup</code>
	 * @param actionDispatcher répartiteur utilisé pour créer une<code>PEAction</code>
	 * @param localizer utilisé pour localiser les paramètre des actions
	 * @param hi une <code>ConvexZoneHitInfo</code> contenant les informations relatives à l'évènement-souris qui a
	 * déclencher le menu contextuel.
	 */
	public java.util.ArrayList<PEConvexZoneAction> createActions(ActionDispatcher actionDispatcher, ActionLocalizer localizer, ConvexZoneHitInfo hi);
}
